
public class DataTypes {
public static void main(String[] args) {
	int a=100;
	char b ='h';
	byte c=10;
	String d= new String("data type program");
	int e[]= {1,2,3,4,5};
	boolean f=true;
	boolean g=false;
	float h=1.2f;
	System.out.println("Integer 'a' is "+a);
	System.out.println("Char 'b' is "+b);
	System.out.println("byte 'c' is "+c);
	System.out.println("String 'd' is "+d);
	for(int i=0;i<5;i++)
	{
	System.out.println("integer array 'a["+i+"] is "+e[i]);
  }
	System.out.println("boolean 'f' is "+f);
	System.out.println("boolean 'g' is "+g);
	System.out.println("float 'h' is "+h);
}
}
